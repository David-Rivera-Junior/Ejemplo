package com.personal.Proyecto_Persona;

public class Camisa {
    private int codigo;
    private String camisa;
    
    public Camisa(){
        
    }
        
        public Camisa(int codigo, String camisa){
            this.codigo = codigo;
            this.camisa = camisa;
            
        }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getCamisa() {
        return camisa;
    }

    public void setCamisa(String camisa) {
        this.camisa = camisa;
    }
            
        }
    
    

