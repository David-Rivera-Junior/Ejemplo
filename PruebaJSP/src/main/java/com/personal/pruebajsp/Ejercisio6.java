package com.personal.pruebajsp;

public class Ejercisio6 {
    public static void main(String[]args){
       
   char v='e';
   
   switch(v){
       case 'a':
       System.out.println("Es vocal");
           break;
           case 'e':
               System.out.println("Es vocal");
               break;
               case 'i':
                   System.out.println("Es vocal");
                   break;
                   case 'o':
                       System.out.println("Es vocal");
                       break;
                       case 'u':
                           System.out.println("Es vocal");
                           break;
                       default:
                           System.out.println("Es consonante");
     
   }
    }
    
}

